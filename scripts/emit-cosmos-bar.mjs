/**
 * emit-cosmos-bar.mjs — build-time emitter for the cosmos bar
 * ════════════════════════════════════════════════════════════════════════════
 * Reads:
 *   - src/data/atlas.json            (canonical cosmos source of truth)
 *   - src/cosmos-bar/styles.css      (PLACEHOLDER + FULL marked blocks)
 *   - src/cosmos-bar/component.ts    (web component definition)
 *   - src/cosmos-bar/icons.ts        (icon library)
 *
 * Writes:
 *   - public/atlas-bar.json          (slim runtime feed, ~2KB)
 *   - public/cosmos-bar.css          (placeholder-only, ~1KB, vendored per repo)
 *   - public/cosmos-bar.js           (bundled web component, ~10KB target)
 *
 * Touched: 9 May 2026 EVE — initial build.
 */

import { readFile, writeFile } from 'node:fs/promises';
import { fileURLToPath } from 'node:url';
import { dirname, resolve } from 'node:path';
import { build } from 'esbuild';

const here = dirname(fileURLToPath(import.meta.url));
const root = resolve(here, '..');
const SRC = resolve(root, 'src');
const PUBLIC = resolve(root, 'public');

// ─── 1. Slim atlas-bar.json ────────────────────────────────────────────────
async function emitAtlasBarJson() {
  const atlas = JSON.parse(await readFile(resolve(SRC, 'data/atlas.json'), 'utf-8'));

  // Bar bodies in display order (same as the full cosmos):
  //   earth · guided (Earth's moon) · brainbar · shift · plainai · curriculum
  //   (Plain AI's moon) · agentic · claw · MCP (relay)
  const bodies = [];

  for (const planet of atlas.planets) {
    if (planet.slug === 'earth') {
      bodies.push(toBarBody(planet, 'planet'));
      // Insert Earth's moons immediately after Earth.
      for (const m of planet.moons || []) bodies.push(toBarBody(m, 'moon'));
      continue;
    }
    if (planet.slug === 'plainai') {
      // Brainbar/shift come BEFORE plainai per atlas order — handled by general loop.
      bodies.push(toBarBody(planet, 'planet'));
      for (const m of planet.moons || []) bodies.push(toBarBody(m, 'moon'));
      continue;
    }
    bodies.push(toBarBody(planet, 'planet'));
  }

  // Always end with the relay (MCP).
  if (atlas.mcp) {
    bodies.push({
      slug: atlas.mcp.slug || 'mcp',
      name: atlas.mcp.name || 'MCP relay',
      tagline: atlas.mcp.tagline || atlas.mcp.subLabel || 'the relay',
      url: atlas.mcp.url || 'https://mcp.aguidetocloud.com/',
      lastShippedAt: atlas.mcp.lastShippedAt || null,
      kind: 'relay',
    });
  }

  const out = {
    bodies,
    generatedAt: new Date().toISOString(),
    _comment:
      'cosmos-bar slim runtime feed — read by https://cosmos.aguidetocloud.com/cosmos-bar.js. SSOT is src/data/atlas.json. Regenerated by scripts/emit-cosmos-bar.mjs on every build.',
  };

  await writeFile(resolve(PUBLIC, 'atlas-bar.json'), JSON.stringify(out, null, 2));
  console.log(`[cosmos-bar] wrote public/atlas-bar.json — ${bodies.length} bodies`);
  return bodies.length;
}

// Canonical planet origins. Atlas.json holds moons' urls + the relay url, but
// planet top-level urls aren't stored there (the cosmos itself uses card-library
// pills for navigation). Bar needs one URL per body, so we keep the mapping here.
// Single-source-of-truth for cosmos-bar: this map.
const PLANET_URLS = {
  earth:    'https://www.aguidetocloud.com/',
  brainbar: 'https://cmd.aguidetocloud.com/',
  shift:    'https://shift.aguidetocloud.com/',
  plainai:  'https://plainai.aguidetocloud.com/',
  agentic:  'https://agents.aguidetocloud.com/',
  claw:     'https://claw.aguidetocloud.com/',
};

function toBarBody(p, kind) {
  // Moons + relay carry their own url in atlas.json; planets get the map above.
  const url = p.url && p.url.length > 0 ? p.url : PLANET_URLS[p.slug] || '';
  return {
    slug: p.slug,
    name: p.name || prettySlug(p.slug),
    tagline: p.tagline || '',
    url,
    lastShippedAt: p.lastShippedAt || null,
    kind,
  };
}

function prettySlug(slug) {
  if (slug === 'plainai')   return 'Plain AI';
  if (slug === 'brainbar')  return 'Brain Bar';
  if (slug === 'mcp')       return 'MCP Relay';
  return slug.charAt(0).toUpperCase() + slug.slice(1);
}

// ─── 2. Split styles.css into placeholder + full blocks ────────────────────
async function splitStyles() {
  const css = await readFile(resolve(SRC, 'cosmos-bar/styles.css'), 'utf-8');

  const placeholder = extractBlock(css, 'PLACEHOLDER');
  const full = extractBlock(css, 'FULL');

  if (!placeholder) throw new Error('PLACEHOLDER-START / PLACEHOLDER-END markers missing in styles.css');
  if (!full) throw new Error('FULL-START / FULL-END markers missing in styles.css');

  await writeFile(
    resolve(PUBLIC, 'cosmos-bar.css'),
    `/* cosmos-bar.css — vendored placeholder. Do not edit here.\n   Source: cosmos-atlas/src/cosmos-bar/styles.css (PLACEHOLDER block).\n   Regenerated by scripts/emit-cosmos-bar.mjs. */\n${placeholder.trim()}\n`
  );
  console.log(`[cosmos-bar] wrote public/cosmos-bar.css — ${placeholder.length} bytes (placeholder)`);

  return full.trim();
}

function extractBlock(css, name) {
  const re = new RegExp(`\\/\\*\\s*${name}-START[\\s\\S]*?\\*\\/([\\s\\S]*?)\\/\\*\\s*${name}-END\\s*\\*\\/`, 'm');
  const m = css.match(re);
  return m ? m[1] : null;
}

// ─── 3. Bundle component.ts → public/cosmos-bar.js ─────────────────────────
async function bundleComponent(fullStylesText) {
  const result = await build({
    entryPoints: [resolve(SRC, 'cosmos-bar/component.ts')],
    bundle: true,
    minify: true,
    format: 'iife',
    target: ['es2020'],
    platform: 'browser',
    define: {
      __INLINE_STYLES__: JSON.stringify(fullStylesText),
    },
    write: false,
    loader: { '.ts': 'ts' },
  });

  const out = result.outputFiles[0];
  if (!out) throw new Error('esbuild produced no output');
  const banner = `/* cosmos-bar.js — single-file web component, served at https://cosmos.aguidetocloud.com/cosmos-bar.js\n   Source: cosmos-atlas/src/cosmos-bar/component.ts\n   Generated: ${new Date().toISOString()} */\n`;
  await writeFile(resolve(PUBLIC, 'cosmos-bar.js'), banner + out.text);
  console.log(`[cosmos-bar] wrote public/cosmos-bar.js — ${out.text.length} bytes (minified IIFE)`);
}

// ─── main ───────────────────────────────────────────────────────────────────
const t0 = Date.now();
const bodyCount = await emitAtlasBarJson();
const fullStyles = await splitStyles();
await bundleComponent(fullStyles);
const elapsed = Date.now() - t0;
console.log(`[cosmos-bar] done in ${elapsed}ms — ${bodyCount} bodies, 3 artifacts emitted`);
